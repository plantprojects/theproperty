        <h1>KILL ALL ZOMBIES: Abstraction for the Living</h1>
        <p>Curated by Adam Novak</p>
        <p>October 11th - November 14th, 2015</p>

        <p>In <i><b>Kill All Zombies</b></i> The Property presents eight California-based artists (Lilly Aldriedge, Cameron Crone, Julia Haft-Candell, John Houck, David Gilbert, John Mills, Kirk Stoller, and David Zuttermeister) who are working with fertile and invigorating forms of abstraction that both engage with abstraction's history and look forward to its future.</p>
        <p>In recent years, contemporary abstraction in painting has been dominated by a formal reductiveness known as "Zombie Formalism". Like a zombie mob, this trend has spread like viral anemia, creating a glut of conceptually and formally thin work across artistic media. This work has infected galleries and art fairs worldwide, nearly overtaking the contemporary dialogue around abstraction.</p>
        <p>While it maybe impossible to define such a sprawling, amorphous trend, it is worth the attempt to describe Zombie Formalism if only to understand how the artists in this show exist outside the tendency. One form of Zombie Formalism is recognizable, first from its tasteful colors: usually some form of monochrome, muted, or limited palette. The abstraction strategy is typically simplistic (grids, dots, patterns, etc) and borrowed from late Modernism with a prevalence for allover fields. The reference towards the Modernist framework is often glib. Moreover the earlier artistic language is not re-explored in depth. Zombie work has a casual, unfinished quality to it - a quiet statement that risk and struggle in the artistic process is dead, or at least outr&eacute;.</p>
        <p>In this context one wonders what rich explorations of contemporary abstraction have remain untraveled. If Zombie Abstraction merely references the history of abstraction, what would it look like if an artist actually worked in dialog with traditions of abstraction in order to build their own personal praxis, crafting visual languages for the present day? <i>Kill All Zombies</i> presents eight possible answers to that question.</p>
        <p>The heterogeneous work of these artists does not make up a cohesive school. What they share is an approach to art making that is interested in discovery and exploration through the art making process. These artists may reference work from older traditions and movements but, unlike the Zombie formalists, they do so in order to help navigate towards innovations in their own personal artistic language. </p>
        <p>Diverging from the modernist ethos, they are not working inside of some historical trajectory towards the "new", but instead are opening up expressive and conceptual possibilities previously glossed over and under-explored in Modernism. Unlike their Zombie contemporaries these artists do not exhume Modernist remains as knowing sign-posts but instead explore the untapped potential that still exists within abstraction.</p>

        <h2>THE ARTISTS AND THEIR WORKS</h2>

        <p><b>John Mills's</b> work <i>Let's Agree to Disagree</i> recalls surrealistic biomorphic abstraction and automatic drawing, as well as, midcentury European Taschisme painting such as Wols. However Mills introduces those influences into a contemporary painting context with an emphasis on a larger late Modernist scale and more complex figure ground relationships. His fragile, searching and sometimes awkward line connects with the mark making of Cy Twombly. There is also a relationship here to American postmodern abstraction, specifically the work of Carroll Dunham and Jonathan Lasker, artists who explored the space between "doodle", "mark", and "figure" in their abstractions. Like Lasker, Mills plays with concepts of expressionism. Although they retain a fresh quality, his paintings are premeditated and evolve out of fast exploratory drawings that are later translated into larger works. </p>

        <p><b>Cameron Crone</b> works in a variety of media but most often in photography. His photos are influenced by the high formalism of photography's past, particularly Edward Weston, but he also incorporates Conceptual and Process art references in his work. Like Weston, Crone creates formally compelling abstractions using everyday objects. In the vertical photograph <i>Overlay 1;</i> he uses kitty litter and beach towels to create a mysterious and disorienting impression. The work recalls midcentury abstract painting, Chinese landscape painting, and even California psychedelia: all references that aim toward some form of transcendence. Moreover in Crone's photograph those references are re-contextualized through the use of abject, common materials. These pictures remind the viewer of abstraction's radical ability to re-orient us towards reality.</p>

        <p><b>Julia Haft-Candell's</b></b> <i>Eye with Letter on Granite</i> feels absolutely contemporary but also neo-Modern, and like many early modernists, pre-classical in influence as well. In this way, the sculpture synthesizes contemporary abstraction with a primordial animal or some ritualistic deity form. It shares similarities with the sculpture of Constantin Brancusi, even including an abstracted pedestal. But Eye with Letter on Granite differentiates itself from its Modernist influences in its unique personal formal language and its use of multiple textures and materials. Unlike many of her Zombie contemporaries, there is a strong commitment to craft with Haft-Candell's work, as well as an engagement with the concept of abstraction as a vehicle for exploration.</p>

        <p><b>David Gilbert's</b> work collapses installation, sculpture, drawing, and painting into photography using an abstract language of assembled objects, inscribed and painted forms, and various other studio detritus. The resulting photographs contain elements that stand in for some form of figuration or an action, which is often bathed in a light that recalls the great Dutch Baroque masters. Gilbert's work Stationary Letter brings together sculpture, photography, installation and text. By presenting the viewer with an abstract sculpture and then representing that sculpture inside of a photograph, Gilbert chips away at the barrier between abstraction and representation. The "handwriting" inside the photograph is at once abstracted and a signifier of handwriting. This further complicates the context of the representational image of the sculpture by adding a level of semiotic abstraction.</p>

        <p><b>Kirk Stoller</b> uses found materials, which he then paints, combines and reassembles into sculpture - often in ways that visually suggest they are delicately balanced together. In his use of found materials to construct his sculpture, Stoller's work builds upon the modernist mid-century Assemblage movement and shares some commonalities with the Postmodern sculpture of artists like Tony Cragg. An important influence is Richard Tuttle, with whom Stoller shares a preference for humble materials and an elegant, poetic form of personal formalism. That poetic form is only heightened with Stoller's visual use of precarious balance. In <i>untitled (cursive)</i>, the sculpture manages to be visually dynamic and playful while also appearing so sensitive that it looks like a strong gust of air could topple it.</p>

        <p><b>Lilly Aldriedge</b> builds on the history of gestural painting and Post-Painterly Abstraction. At the same time, her mark making and closed-off process evoke the paintings of David Reed. The painting <i>Fan</i> uses a form that is mostly enclosed by a border. This device is rendered transparent, revealing washy strokes below or inside, while forms in relief are added on top of the picture plane. These competing formal and structural elements coalesce into a cohesive image that challenges the viewer to explore the complex nature of space and process inside the image. As opposed to a typical Zombie Abstraction, there is much to be explored inside the picture, as different formal elements are revealed over time.</p>

        <p><b>David Zuttermeister's</b> sculpture <i>Untitled (Animal Magnet)</i> asserts itself with a nervous, frenetic energy despite its static state. Its structure seems at once rigid yet flexible. The form is reminiscent of the letter "H" while also appearing anthropomorphic in a way that recalls the sculptures of Joel Shapiro. Unlike Shapiro, Zuttermeister's sculptures lean more towards the organic then the geometric and are further complicated by his unique use of textures and his almost playful use of color. Zuttermeister builds his sculptures off of metal supports and transforms his material as he builds up the layers of his forms. His commitment to craft and his exploratory art making process differentiates him from the deskilled cynicism of Zombie formalism.</p>

        <p>Working with still-life, a format that has been no stranger to abstraction historically, <b>John Houck</b> builds his photographs of objects using a complex and layered process. Houck is influenced by psychoanalysis and he collects objects connected to his personal history, which he then arranges, photographs and re-photographs. The results are subtle abstracted images that recall Paul Cezanne and Giorgio Morandi in their sensitive manipulation of space. In <i>Decorated Shed</i>, Houck composes his work with drawings based off a Pablo Picasso piece. The reference here is to a project of his on Post-Modernism that he completed as an architectural student. This postmodern content is elaborated by the title of the work and Houck's use of the ducks, which are both references to Robert Venturi. All of this takes place in a visually ambiguous location that alternates between depth and flatness, as colored paper forms arrange and then distort the visual space</p>
        <p>---Adam Novak</p>
